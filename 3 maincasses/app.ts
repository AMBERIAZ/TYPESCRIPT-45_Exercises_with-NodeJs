let personName: string = "Amber"; // This saves the person name

//lowercase
console.log("lowercase:" , personName.toLowerCase()); // Shows the person name in all small letters
console.log("uppercase:" , personName.toUpperCase()); // Shows the  person name in ALL BIG LETTERS
console.log("titlecase:" , personName.charAt(0).toUpperCase() + personName.slice(1).toLowerCase()); // Shows the  person name with the First Letter Big